class User {
    constructor(Id, CreatedDatetime, Value, Email, Name,
        Password, Role, Type, CompanyRegistrationNumber, Surname,
        UserName, ControlAreaREF, FacilityREF

    ) {
        this.Id = Id;
        this.CreatedDatetime = CreatedDatetime;
        this.Value = Value;
        this.Email = Email;
        this.Name = Name;
        this.Password = Password;
        this.Role = Role;
        this.Type = Type;
        this.CompanyRegistrationNumber = CompanyRegistrationNumber;
        this.Surname = Surname;
        this.UserName = UserName;
        this.ControlAreaREF = ControlAreaREF;
        this.FacilityREF = FacilityREF;
    }
}